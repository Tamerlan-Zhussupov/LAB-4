    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int x = Integer.valueOf(jTextField2.getText());
        Integer [][] a = new Integer[x][x];
        Random rn = new Random();
        for (int i = 0; i < x; i++)
        {
            for (int j = 0; j < x; j++)
            {
                a[i][j] = rn.nextInt(100);
            }
        }
        Integer [][] b = new Integer[x][x];
        jTable1.setModel(new javax.swing.table.DefaultTableModel(a, new String[x]));
        jTable2.setModel(new javax.swing.table.DefaultTableModel(b, new String[x]));
        int min = a[0][0];
        for (int i = 0; i < x; i++)
        {
            for (int j = 0; j < x; j++)
            {
                if (a[i][j] < min & i == j)
                {
                    min = a[i][j];
                }
            }
        }
        jTextField1.setText(Integer.toString(min));
        for (int i = 0; i < x; i++)
        {
            int sum = 0;
            for (int j = 0; j < x; j++)
            {
                sum = sum + a[j][i];
            }
            jTable2.setValueAt(sum, 0, i);
            jTable2.setValueAt(sum / min, 1, i);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

